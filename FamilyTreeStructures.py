class Node(object):
    def __init__(self, name):
        self.name = str(name)
    def getName(self):
        return self.name
    def __str__(self):
        return self.name
    def __repr__(self):
        return self.name
    def __ne__(self, other):
        return not self.__eq__(other)
    def __hash__(self):
        # Override the default hash method
        # Think: Why would we want to do this?
        return self.name.__hash__()

class Edge(object):
    def __init__(self, src, dest):
        self.src = src
        self.dest = dest
    def getSource(self):
        return self.src
    def getDestination(self):
        return self.dest
    def __str__(self):
        return '{0}->{1}'.format(self.src, self.dest)

class Digraph(object):
    """
    A directed graph
    """
    def __init__(self):
        # A Python Set is basically a list that doesn't allow duplicates.
        # Entries into a set must be hashable (where have we seen this before?)
        # Because it is backed by a hashtable, lookups are O(1) as opposed to the O(n) of a list (nifty!)
        # See http://docs.python.org/2/library/stdtypes.html#set-types-set-frozenset
        self.nodes = set([])
        self.edges = {}
    def addNode(self, node):
        if node in self.nodes:
            # Even though self.nodes is a Set, we want to do this to make sure we
            # don't add a duplicate entry for the same node in the self.edges list.
            raise ValueError('Duplicate node')
        else:
            self.nodes.add(node)
            self.edges[node] = []
    def addEdge(self, edge):
        src = edge.getSource()
        dest = edge.getDestination()
        if not(src in self.nodes and dest in self.nodes):
            raise ValueError('Node not in graph')
        self.edges[src].append(dest)
    def childrenOf(self, node):
        return self.edges[node]
    def hasNode(self, node):
        return node in self.nodes
    def __str__(self):
        res = ''
        for k in self.edges:
            for d in self.edges[str(k)]:
                res = '{0}{1}->{2}\n'.format(res, k, d)
        return res[:-1]
    
class WeightedEdge(Edge):
    def __init__(self, src, dest, total, outside):
        Edge.__init__(self, src, dest)
        self.total = total
        self.outside = outside
    def getTotalDistance(self):
        return self.total
    def getOutdoorDistance(self):
        return self.outside
    def __str__(self):
        return '{0}->{1}'.format(self.src, self.dest) + ' (' + str(self.total) + ', ' + str(self.outside) + ')'

class WeightedDigraph(Digraph):
    def __init__(self):
        Digraph.__init__(self)
        self.edgeweights = {}
        self.nodes = set([])
        self.edges = {}

    def addNode(self, node):
        if node in self.nodes:
            raise ValueError('Duplicate node')
        else:
            self.nodes.add(node)
            self.edges[node] = []
            self.edgeweights[node] = []

    def addEdge(self, edge):
        src = edge.getSource()
        dest = edge.getDestination()
        total = edge.getTotalDistance()
        outside = edge.getOutdoorDistance()
        if not (Node(src) in self.nodes and Node(dest) in self.nodes):
            print(src in self.nodes)
            raise ValueError('Node not in graph')
        self.edgeweights[Node(src)].append([dest, (total, outside)])

    def childrenOf(self, node):
        empty = {}
        D = self.edgeweights
        for key in D:
            Y = D[Node(key)]
            empty[Node(key)] = []
            for i in range(len(Y)):
                empty[Node(key)].append(Y[i][0])

        return empty[Node(key)]

    def __str__(self):
        res = ''
        for k in self.edgeweights: 
            for i in range(len(self.edgeweights[k])): 
                    res = '{0}{1}->{2} ({3}, {4}) \n'.format(res, k, self.edgeweights[k][i][0], float(self.edgeweights[k][i][1]), float(self.edgeweights[k][i][2]))
        return res[:-1]


